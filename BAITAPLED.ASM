ORG 0000H
MAIN:
    CHOPTAT:
	    MOV R0,#3
    aaa:MOV P1,#0FFH
	    CALL DELAY
		MOV P1,#00H
		CALL DELAY
		DJNZ R0,aaa
		LJMP DICH1
	RET
	DICH1:
	    MOV R0,#8
		MOV A,#0FEH
	bbb:MOV P1,A
	    CALL DELAY
		RL A
		DJNZ R0,bbb
		LJMP DICH2
	RET
	DICH2:
	    MOV R0,#7
		MOV A,#3FH
	ccc:MOV P1,A
	    CALL DELAY
		RR A
		DJNZ R0,ccc
		LJMP SANGDAN
	RET
	SANGDAN:
	    MOV R0,#9
		MOV A,#0FFH
	ddd:MOV P1,A
	    CALL DELAY
		CLR C
		RLC A
		DJNZ R0,ddd
		LJMP TATDAN
	RET
	TATDAN:
	    MOV R0,#9
		MOV A,#00H
	eee:MOV P1,A
	    CALL DELAY
		SETB C
		RRC A
		DJNZ R0,eee
		LJMP CHOPTAT
	RET
	DELAY:
	    MOV R5,#5
	LAP2:MOV R6,#200
	LAP1:MOV R7,#250
	    DJNZ R7,$
		DJNZ R6,LAP1
		DJNZ R5,LAP2
	RET
END
		